let a = 5;
let b = 7;

console.log(a + b, "Suma");
console.log(a - b, "Resta");
console.log(a * b, "Multiplicacion");
console.log(a / b, "Division");
console.log(a % b, "Modulo");
console.log(a ** b, "Potencia");

// Incrementar
console.log(++a); // El numero incrementa antes de ser imprimido
console.log(a++); // El numero no se imprime aumentado pero aumenta despues de la impresion
console.log(a);
// Drecementar
console.log(--a);
console.log(a--);
console.log(a);