// Operadores de comparacion relacionales
let a = 10;
console.log(a > 5); // True
console.log(a >= 5); // True
console.log(a < 5); // False
console.log(a <= 5); // False

// Operadores de comparacion de igualdad
console.log(a == 10);
console.log(a != 10);
console.log(a == '10'); // Es true esto se debe a que JS solo compara los valores cuando se usa ==
console.log(a === '10'); // A diferencia de arriba este compara tambien el tipo de valor de cada uno
console.log(a !== '10');